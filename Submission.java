public class Submission
{
    private int id;
    private int problem;
    private int veredict;
    private int runtime;
    private int memory;
    private int time;
    private int lang;
    private int rank;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the problem
     */
    public int getProblem() {
        return problem;
    }

    /**
     * @param problem the problem to set
     */
    public void setProblem(int problem) {
        this.problem = problem;
    }

    /**
     * @return the veredict
     */
    public int getVeredict() {
        return veredict;
    }

    /**
     * @param veredict the veredict to set
     */
    public void setVeredict(int veredict) {
        this.veredict = veredict;
    }

    /**
     * @return the runtime
     */
    public int getRuntime() {
        return runtime;
    }

    /**
     * @param runtime the runtime to set
     */
    public void setRuntime(int runtime) {
        this.runtime = runtime;
    }

    /**
     * @return the memory
     */
    public int getMemory() {
        return memory;
    }

    /**
     * @param memory the memory to set
     */
    public void setMemory(int memory) {
        this.memory = memory;
    }

    /**
     * @return the time
     */
    public int getTime() {
        return time;
    }

    /**
     * @param time the time to set
     */
    public void setTime(int time) {
        this.time = time;
    }

    /**
     * @return the lang
     */
    public int getLang() {
        return lang;
    }

    /**
     * @param lang the lang to set
     */
    public void setLang(int lang) {
        this.lang = lang;
    }

    /**
     * @return the rank
     */
    public int getRank() {
        return rank;
    }

    /**
     * @param rank the rank to set
     */
    public void setRank(int rank) {
        this.rank = rank;
    }

    
}
